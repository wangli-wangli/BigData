package wl;

public class copy {
	private int id;
	private int yuan_id;
	private String copy_field;
	private int start_position;
	private int last_position;
	private String url;
	
	public int getStart_position() {
		return start_position;
	}
	public void setStart_position(int start_position) {
		this.start_position = start_position;
	}
	public int getLast_position() {
		return last_position;
	}
	public void setLast_position(int last_position) {
		this.last_position = last_position;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getYuan_id() {
		return yuan_id;
	}
	public void setYuan_id(int yuan_id) {
		this.yuan_id = yuan_id;
	}
	public String getCopy_field() {
		return copy_field;
	}
	public void setCopy_field(String copy_field) {
		this.copy_field = copy_field;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	

}
